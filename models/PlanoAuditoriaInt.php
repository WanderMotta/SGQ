        $filter = $curfilter ? $this->CurrentFilter : "";
        if (is_array($where)) {
            $where = $this->arrayToFilter($where);
        }
        AddFilter($filter, $where);
        if ($filter != "") {
            $queryBuilder->where($filter);
        }
        return $queryBuilder;
    }

    // Update
    public function update(&$rs, $where = "", $rsold = null, $curfilter = true)
    {
        // Cascade Update detail table 'item_plano_aud_int'
        $cascadeUpdate = false;
        $rscascade = [];
        if ($rsold && (isset($rs['idplano_auditoria_int']) && $rsold['idplano_auditoria_int'] != $rs['idplano_auditoria_int'])) { // Update detail field 'plano_auditoria_int_idplano_auditoria_int'
            $cascadeUpdate = true;
            $rscascade['plano_auditoria_int_idplano_auditoria_int'] = $rs['idplano_auditoria_int'];
        }
        if ($cascadeUpdate) {
            $rswrk = Container("item_plano_aud_int")->loadRs("`plano_auditoria_int_idplano_auditoria_int` = " . QuotedValue($rsold['idplano_auditoria_int'], DataType::NUMBER, 'DB'))->fetchAllAssociative();
            foreach ($rswrk as $rsdtlold) {
                $rskey = [];
                $fldname = 'iditem_plano_aud_int';
                $rskey[$fldname] = $rsdtlold[$fldname];
                $rsdtlnew = array_merge($rsdtlold, $rscascade);
                // Call Row_Updating event
                $success = Container("item_plano_aud_int")->rowUpdating($rsdtlold, $rsdtlnew);
                if ($success) {
                    $success = Container("item_plano_aud_int")->update($rscascade, $rskey, $rsdtlold);
                }
                if (!$success) {
                    return false;
                }
                // Call Row_Updated event
                Container("item_plano_aud_int")->rowUpdated($rsdtlold, $rsdtlnew);
            }
        }

        // If no field is updated, execute may return 0. Treat as success
        try {
            $success = $this->updateSql($rs, $where, $curfilter)->executeStatement();
            $success = $success > 0 ? $success : true;
            $this->DbErrorMessage = "";
        } catch (\Exception $e) {
            $success = false;
            $this->DbErrorMessage = $e->getMessage();
        }

        // Return auto increment field
        if ($success) {
            if (!isset($rs['idplano_auditoria_int']) && !EmptyValue($this->idplano_auditoria_int->CurrentValue)) {
                $rs['idplano_auditoria_int'] = $this->idplano_auditoria_int->CurrentValue;
            }
        }
        return $success;
    }

