    // Render row
    public function renderRow()
    {
        global $Security, $Language, $Language;
        $conn = $this->getConnection();
        if ($this->RowType == RowType::TOTAL && $this->RowTotalSubType == RowTotal::FOOTER && $this->RowTotalType == RowSummary::PAGE) {
            // Build detail SQL
            $firstGrpFld = &$this->fatores;
            $firstGrpFld->getDistinctValues($this->GroupRecords);
            $where = DetailFilterSql($firstGrpFld, $this->getSqlFirstGroupField(), $firstGrpFld->DistinctValues, $this->Dbid);
            AddFilter($where, $this->Filter);
            $sql = $this->buildReportSql($this->getSqlSelect(), $this->getSqlFrom(), $this->getSqlWhere(), $this->getSqlGroupBy(), $this->getSqlHaving(), $this->getSqlOrderBy(), $where, $this->Sort);
            $rs = $sql->executeQuery();
            $records = $rs?->fetchAll() ?? [];
            $this->analise->getCnt($records, false);
            $this->PageTotalCount = count($records);
        } elseif ($this->RowType == RowType::TOTAL && $this->RowTotalSubType == RowTotal::FOOTER && $this->RowTotalType == RowSummary::GRAND) { // Get Grand total
            $hasCount = false;
            $hasSummary = false;

            // Get total count from SQL directly
            $sql = $this->buildReportSql($this->getSqlSelectCount(), $this->getSqlFrom(), $this->getSqlWhere(), $this->getSqlGroupBy(), $this->getSqlHaving(), "", $this->Filter, "");
            $rstot = $conn->executeQuery($sql);
            if ($rstot && $cnt = $rstot->fetchOne()) {
                $hasCount = true;
            } else {
                $cnt = 0;
            }
            $this->TotalCount = $cnt;

            // Get total from SQL directly
            $sql = $this->buildReportSql($this->getSqlSelectAggregate(), $this->getSqlFrom(), $this->getSqlWhere(), $this->getSqlGroupBy(), $this->getSqlHaving(), "", $this->Filter, "");
            $sql = $this->getSqlAggregatePrefix() . $sql . $this->getSqlAggregateSuffix();
            $rsagg = $conn->fetchAssociative($sql);
            if ($rsagg) {
                $this->analise->Count = $this->TotalCount;
                $this->analise->CntValue = $rsagg["cnt_analise"];
                $this->impacto_idimpacto->Count = $this->TotalCount;
                $this->contexto_idcontexto->Count = $this->TotalCount;
                $hasSummary = true;
            }

            // Accumulate grand summary from detail records
            if (!$hasCount || !$hasSummary) {
                $sql = $this->buildReportSql($this->getSqlSelect(), $this->getSqlFrom(), $this->getSqlWhere(), $this->getSqlGroupBy(), $this->getSqlHaving(), "", $this->Filter, "");
                $rs = $sql->executeQuery();
                $this->DetailRecords = $rs?->fetchAll() ?? [];
                $this->analise->getCnt($this->DetailRecords, false);
            }
        }

        // Call Row_Rendering event
        $this->rowRendering();

        // fatores

        // ponto

        // analise

        // impacto_idimpacto

        // contexto_idcontexto
        if ($this->RowType == RowType::SEARCH) {
            // contexto_idcontexto
            $curVal = trim(strval($this->contexto_idcontexto->AdvancedSearch->SearchValue));
            if ($curVal != "") {
                $this->contexto_idcontexto->AdvancedSearch->ViewValue = $this->contexto_idcontexto->lookupCacheOption($curVal);
            } else {
                $this->contexto_idcontexto->AdvancedSearch->ViewValue = $this->contexto_idcontexto->Lookup !== null && is_array($this->contexto_idcontexto->lookupOptions()) && count($this->contexto_idcontexto->lookupOptions()) > 0 ? $curVal : null;
            }
            if ($this->contexto_idcontexto->AdvancedSearch->ViewValue !== null) { // Load from cache
                $this->contexto_idcontexto->EditValue = array_values($this->contexto_idcontexto->lookupOptions());
            } else { // Lookup from database
                if ($curVal == "") {
                    $filterWrk = "0=1";
                } else {
                    $filterWrk = SearchFilter($this->contexto_idcontexto->Lookup->getTable()->Fields["idcontexto"]->searchExpression(), "=", $this->contexto_idcontexto->AdvancedSearch->SearchValue, $this->contexto_idcontexto->Lookup->getTable()->Fields["idcontexto"]->searchDataType(), "");
                }
                $sqlWrk = $this->contexto_idcontexto->Lookup->getSql(true, $filterWrk, '', $this, false, true);
                $conn = Conn();
                $config = $conn->getConfiguration();
                $config->setResultCache($this->Cache);
                $rswrk = $conn->executeCacheQuery($sqlWrk, [], [], $this->CacheProfile)->fetchAll();
                $ari = count($rswrk);
                $arwrk = $rswrk;
                foreach ($arwrk as &$row) {
                    $row = $this->contexto_idcontexto->Lookup->renderViewRow($row);
                }
                $this->contexto_idcontexto->EditValue = $arwrk;
            }
            $this->contexto_idcontexto->PlaceHolder = RemoveHtml($this->contexto_idcontexto->caption());
        } elseif ($this->RowType == RowType::TOTAL && !($this->RowTotalType == RowSummary::GROUP && $this->RowTotalSubType == RowTotal::HEADER)) { // Summary row
