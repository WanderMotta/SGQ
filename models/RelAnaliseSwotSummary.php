            }

            // fatores
            if (strval($this->fatores->groupValue()) != "") {
                $this->fatores->GroupViewValue = $this->fatores->optionCaption($this->fatores->groupValue());
            } else {
                $this->fatores->GroupViewValue = null;
            }
